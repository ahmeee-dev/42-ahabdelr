#include "philo.h"

int	mytoi(char *str)
{
	int	res;
	int	i;

	i = 0;
	res = 0;
	if (str[i] == ' ')
		return (-1);
	while (str[i] != '\0')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return (res);
}