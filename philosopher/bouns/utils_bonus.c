#include "philo_bonus.h"

int	mytoi(char *str)
{
	int	res;
	int	i;

	i = 0;
	res = 0;
	while (str[i] != '\0')
	{
		if (str[i] > '9' || str[i] < '0')
			return (-2);
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return (res);
}
